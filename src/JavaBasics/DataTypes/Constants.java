package JavaBasics.DataTypes;

public class Constants {
    public static void main(String[] args) {
        final int final_variable = 23; // the final reserved keyword will not allow the variable to be changed and will
        // lock it at the value it was first assigned.

        // This should be used in cases where you will not change a value of a variable, like the value of pi for example.
        // pi will always be 3.14
    }
}
